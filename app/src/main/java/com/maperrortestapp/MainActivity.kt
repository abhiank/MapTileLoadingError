import com.google.android.gms.maps.CameraUpdateFactory
import com.google.android.gms.maps.GoogleMap
import com.google.android.gms.maps.OnMapReadyCallback
import com.google.android.gms.maps.SupportMapFragment
import com.google.android.gms.maps.model.*

internal class MainActivity : AppCompatActivity(), OnMapReadyCallback {

    private lateinit var mMap: GoogleMap
    private var index = 0
    private var marker: Marker? = null

    private val navCurrentLocationMarkerBitmap: BitmapDescriptor by lazy {
        var bitmap: Bitmap = BitmapFactory.decodeResource(resources, R.drawable.ic_nav_current_loc)
        bitmap = bitmap.copy(Bitmap.Config.ARGB_8888, true)
        bitmap = Bitmap.createScaledBitmap(
            bitmap,
            resources.getDimension(R.dimen.nav_current_loc_marker_height_width).toInt(),
            resources.getDimension(R.dimen.nav_current_loc_marker_height_width).toInt(),
            true
        )
        BitmapDescriptorFactory.fromBitmap(bitmap)
    }

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_main)
        val mapFragment = supportFragmentManager
            .findFragmentById(R.id.map) as SupportMapFragment
        mapFragment.getMapAsync(this)
    }

    override fun onMapReady(googleMap: GoogleMap) {
        mMap = googleMap
        moveToNextLocation()
    }

    /*
    This method is looping over the location list and moving the camera to a new
    location every 1000 milliseconds. This is to simulate a situation where the map
    is following a real vehicle and location updates are received every 1000 milliseconds.
     */
    private fun moveToNextLocation() {
        Handler(Looper.getMainLooper()).postDelayed(1000) {

            if (index == locationList.size) index = 0

            val location = locationList[index++].toLatLng()

            val markerOptions =
                MarkerOptions().position(location).draggable(false)
                    .icon(navCurrentLocationMarkerBitmap).flat(true)
                    .rotation(0f)
                    .anchor(0.5f, 0.5f)

            marker?.remove()
            marker = mMap.addMarker(markerOptions)

            val cameraPosition = CameraPosition.Builder()
                .target(location)
                .zoom(20f)
                .tilt(50f)
                .bearing(0f)
                .build()

            mMap.animateCamera(CameraUpdateFactory.newCameraPosition(cameraPosition))

            moveToNextLocation()
        }
    }
}

